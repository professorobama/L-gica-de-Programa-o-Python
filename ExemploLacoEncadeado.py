#Exemplo de Laço encadeado
#Entrada de um número e verifica se ele é igual as condições

numero = int(input("Digite um numero: "))

if numero == 10:
    print("Número digitado corresponde ao 10")
elif numero == 20:
    print("Número digitado corresponde ao 20")     
elif numero == 30:
    print("Número digitado corresponde ao 30")
elif numero == 40:
    print("Número digitado corresponde ao 40")
else:
    print("O número digitado é diferente de 10,20,30 e 40")